ll check(ll mid) {
	ll sum = 0, og = mid;
	while (mid > 0) {
		sum += mid % 10;
		mid /= 10;
	}
	//cout << sum << nL;
	return gcd(og, sum) > 1;
}

ll gcd(ll a, ll b) {
	if (a == 0)
		return b;
	return gcd(b%a, b);
}

